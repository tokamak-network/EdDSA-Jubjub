pub fn poseidon13(inputs: &[BigInt]) -> Result<BigInt, String> {
    if inputs.len() != 13 {
        return Err(format!(
            "Expected an array with 13 elements, but got {}",
            inputs.len()
        ));
    }

    let result = poseidon_permutation(inputs, 8, 57, &ROUND_CONSTANTS, &MDS_MATRIX);

    result
        .first()
        .cloned()
        .ok_or_else(|| "Permutation returned empty result".to_string())
}

